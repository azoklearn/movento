export function normalizeEmail(email) {
  return String(email || "").trim().toLowerCase();
}

export function isSafePromptFile(file) {
  return typeof file === "string" && /^[a-zA-Z0-9_.-]+\.md$/.test(file);
}

export function getClientUrl(req) {
  const configured = process.env.CLIENT_URL || process.env.VERCEL_URL;
  if (configured) {
    return configured.startsWith("http") ? configured : `https://${configured}`;
  }

  const proto = req.headers["x-forwarded-proto"] || "https";
  return `${proto}://${req.headers.host}`;
}

export function extractPrompt(md) {
  const heading = md.match(/^##\s*.*Prompt\s*$/im);
  if (!heading || heading.index === undefined) return md.trimEnd();
  let after = md.slice(heading.index + heading[0].length);
  const end = after.indexOf("* * *");
  if (end >= 0) after = after.slice(0, end);
  return after
    .replace(/^\s*\n/, "")
    .replace(/\n\*?Generated by MotionSites Export Tool\*?[\s\S]*$/g, "")
    .replace(/\n---\s*$/g, "")
    .replace(/^```(?:text)?\s*\n/i, "")
    .replace(/\n```\s*$/g, "")
    .trimEnd();
}

export async function customerHasStripeAccess(email) {
  const normalizedEmail = normalizeEmail(email);
  if (!normalizedEmail || !process.env.STRIPE_SECRET_KEY) return false;

  try {
    const customers = await stripe.customers.list({ email: normalizedEmail, limit: 10 });

    for (const customer of customers.data) {
      const subscriptions = await stripe.subscriptions.list({
        customer: customer.id,
        status: "active",
        limit: 5,
      });
      if (subscriptions.data.length > 0) return true;

      const charges = await stripe.charges.list({
        customer: customer.id,
        limit: 10,
      });
      if (charges.data.some((c) => c.paid && !c.refunded)) return true;
    }
  } catch (error) {
    console.error("Stripe customerHasStripeAccess error:", error);
    return false;
  }

  return false;
}

